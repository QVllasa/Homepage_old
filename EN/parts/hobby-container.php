                <div class="col-10 col-sm-10 col-md-8  blocktext">
                    <p>For almost 3 years now, trading currencies has been one of my passions. It may sound speculative
                        and risky, but I was fascinated by it in my early youth. At that time I had no financial means
                        to venture into such issues, but after my move to Karlsruhe I dared to enter the world of the
                        financial markets together with a friend from university. We both knew that it would be a
                        high-risk game, but as economists, we didn't think such topics would be particularly alien to
                        us, at least we thought... What I have learnt on the side in recent years cannot be compared to
                        any lecture at university. In the course of time, we have proven ourselves to be so-called
                        'swing traders', as we soon had no nerve to check the courses every ten minutes. The difference
                        to a so-called 'day trader' is that he trades from day to day and only uses very small 'time
                        frames'. I, on the other hand, only use larger timeframes as 'swing traders', which start with
                        the 4-hour chart upwards. I have specialized mainly in Forex trading, where profits can be made
                        from price fluctuations of currencies by buying (long) and selling (short). For my chart
                        analyses, I mainly use resistance and support zones, as well as moving averages and the Relative
                        Strength Index (RSI). I started with intensive Fibonacci analysis and counting waves according
                        to the Elliot Wave Theory, but these methods turned out to be too complex and unreliable, which
                        is why I stayed with the simpler analysis methods. It's the thrill that goes with this matter
                        all the time. So I will probably stay at this ball for a long time to come, not only for the
                        sake of money, but also because of the skills I have learned. I learned the hard way how
                        important sensible risk management is. The university could never have taught me that. Since
                        then I have been able to suppress my emotions more specifically, because they have a strong
                        influence on a buying or selling decision. But I can also transfer this ability to my way of
                        working at the same time, in which I am now able to better separate emotions and decisions from
                        each other. For me, prices are now nothing more than numbers. Profits today also inspire me less
                        with a feeling of happiness. At the same time, I am no longer as angry about losses as I was at
                        the beginning. Once my stop is set, it is also consistently adhered to in order to avoid large
                        losses. So I hang myself from trade to trade at longer intervals of about one week to one month.
                        Most of all I trade the Euro, the US Dollar and the Swiss Franc.</p>
