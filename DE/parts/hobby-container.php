          <h1>Trading</h1>
        </div>
      </div>
      <div class="row justify-content-center text-justify pb-5">
        <div class="col-10 col-sm-10 col-md-8  blocktext">
          <p>Seit nunmehr fast 3 Jahre gehört das Traden von Währungen zu einer meiner Leidenschaften. Es mag sich
            spekulativ und riskant anhören, doch der Reiz hat mich schon in der frühen Jugend gepackt. Damals hatte ich
            noch keine finanziellen Mittel zu Hand, um mich an solchen Themen heranzuwagen, doch
            nach meinem Umzug nach Karlsruhe habe ich mich gemeinsam mit einem Studienfreund
            in die Welt der Finanzmärkte getraut. Wir wussten beide, dass es ein hochriskantes
            Spiel sein wird, aber als Ökonomen sollten uns solche Themen ja auch nicht sonderlich
            fremd sein, dachten wir jedenfalls… Was ich hierbei in den letzten Jahren
            nebenher gelernt habe, kann man mit keiner Vorlesung an der Uni vergleichen. Wir
            haben uns im Laufe der Zeit als sogenannte ‚Swing Trader‘ erwiesen, da wir
            schnell auch keinen Nerv mehr dafür hatten alle zehn Minuten die Kurse zu
            überprüfen. Der Unterschied zu einem sogenannten ‚Daytrader‘ ist, dass dieser von
            Tag zu Tag handelt und nur sehr kleine ‚Timeframes‘ verwendet. Ich dagegen
            verwende als ‚Swing Trader‘ nur größere Timeframes, die mit dem 4-Stunden Chart
            aufwärts beginnen. Spezialisiert habe ich mich vor allem auf das Forex-Trading,
            bei dem aus Kursschwankungen von Währungen, über das Kaufen (Long) und
            Verkaufen (Short), Gewinne erzielt werden können. Für meine Chart-Analysen
            verwende ich hauptsächlich Widerstands- und Unterstützungszonen, sowie die
            Moving Averages und den Relative Strength Index (RSI). Angefangen habe ich mit
            intensiven Fibonacci-Analysen und das zählen von sogenannten ‚Waves‘ gemäß der ‚Elliot-Wave-Theorie‘,
            aber diese Methoden haben sich als zu aufwendig und wenig zuverlässig
            herausgestellt, weshalb ich doch bei den einfacheren Analyse-Methoden verblieben
            bin. Es ist der Nervenkitzel, der ständig mit dieser Materie einhergeht. So
            werde ich wahrscheinlich noch auch lange Zeit nicht nur zwecks des Geldes an
            diesem Ball bleiben, sondern auch aufgrund der erlernten Fähigkeiten. Ich habe auf
            die harte Tour gelernt, wie wichtig eigentlich ein vernünftiges
            Risikomanagement ist. Die Uni hätte mir das nie beibringen können. Ich bin
            seither in der Lage meine Emotionen gezielter zu unterdrücken, denn diese nehmen
            starken Einfluss auf eine Kauf- bzw. Verkaufsentscheidung. Diese Fähigkeit kann
            ich aber auch zeitgleich auf meine Arbeitsweise übertragen, in der ich nun in
            der Lage bin Emotionen und Entscheidungen besser voneinander zu trennen. Für
            mich sind die Preise mittlerweile nichts weiter als Zahlen. Auch Gewinne beflügeln
            mich heute weniger mit einem Glücksgefühl. Gleichzeitig bin ich über Verluste
            auch nicht mehr so verärgert wie zu Beginn. Ist mein Stopp einmal gesetzt, wird
            dieser auch konsequent eingehalten, um große Verluste zu vermeiden. So hangle
            ich mich in größeren Zeitabständen von ca. einer Woche bis einen Monat von
            Trade zu Trade.  Am meisten trade ich den
            Euro, den US Dollar und den Schweizer Franken. </p>
